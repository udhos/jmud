package jmud.util;

public class Separators {
  static public final String BOR = "#";    // Begin of Register
  static public final String EOF = "%";    // End of File
  static public final String EOR = "$";    // End of Register
  static public final String EOD = "~";    // End of Description
  static public final String REM = ";";    // Remark (comment)
  static public final String NL  = "\r\n"; // New Line
  static public final String EOC = "!";    // End of Contents

  static public final char SLASH = '/';
}
